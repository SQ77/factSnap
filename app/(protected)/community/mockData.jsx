export const FILTER_TABS = ['All Posts', 'Urgent', 'Scams', 'Recent'];

// filter for posts
export const getFilteredPosts = (selectedTab) => {
  if (selectedTab === 'All Posts') 
    return mockPosts;
  if (selectedTab === 'Urgent') 
    return mockPosts.filter(post => post.tags.some(tag => tag.includes('Urgent'))
  );
  if (selectedTab === 'Recent') 
    return mockPosts.filter(post => post.date.includes('hour')
  );
  if (selectedTab === 'Scams') 
    return mockPosts.filter(post => post.tags.some(tag => tag.toLowerCase().includes('scam'))
  );
  return mockPosts;
};

export const getPostById = (id) => mockPosts.find(post => post.id === id);
